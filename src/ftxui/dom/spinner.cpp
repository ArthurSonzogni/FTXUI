Element spinner(int c, size_t index) {
  if (c == 0) {
    index %= 40;
    if (index > 20)
      index = 40 - index;
    return gauge(index * 0.05);
  }
  c %= elements.size();
  index %= elements[c].size();
  std::vector<Element> lines;
  for (const auto& it : elements[c][index])
    lines.push_back(text(it));
  return vbox(std::move(lines));
}

